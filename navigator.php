                <h4><a>维基百科-乙型肝炎</a></h4>
                <p>乙型病毒性肝炎，简称乙肝，是一种由乙型肝炎病毒引起的疾病。乙型肝炎病毒会引起肝脏病变。乙肝主要在中国及其他一些亚洲国家中流行。目前中国人口中约有十分之一是乙型肝炎带原者。
                    乙型肝炎与肺结核和艾滋病并列世界上最常见的传染病。乙型肝炎是全球死亡原因的第10位，全世界约有3.5～4亿人感染乙型肝炎病毒，人数高达艾滋病感染者的八倍以上[1]......
