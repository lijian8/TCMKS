                <h4><a>病毒性肝炎</a></h4>
                <p>病毒性肝炎(viral hepatitis)是由多种肝炎病毒引起的，以肝脏损害为主的一组全身性传染病。目前按病原学明确分类的有甲型肝炎(hepatitis A)、乙型肝炎(hepatitis B)、丙型肝炎(hepatitis C)、丁型肝炎(hepatitis D)、戊型肝炎(hepatitis E)。各型病毒性肝炎临床表现相似，以疲乏、食欲减退、肝大、肝功能异常为主，部分病例出现黄疸，但无症状感染亦常见。甲型肝炎和戊型肝炎经消化道途径传播，主要表现为急性肝炎；乙型肝炎、丙型肝炎、丁型肝炎主要经血液、体液等胃肠外途径传播，临床上大部分患者呈慢性感染，少数病例可发展为肝硬化、重型肝炎（肝衰竭）或肝细胞癌......</p>

                <h4><a>丙型肝炎临床路径</a></h4>
                <p>急性丙型肝炎的诊断:①．流行病学史：有输血史、应用血液制品史或明确的HCV暴露史。输血后急性 丙型肝炎的潜伏期为2～16周（平均7周），散发性急性丙型肝炎的潜伏期尚待研究 。②．临床表现：全身乏力、食欲减退、恶心和右季肋部疼痛等，少数伴低热， 轻度肝肿大，部分患者可出现脾肿大，少数患者可出现黄疸。部分患者无明显症状 ，表现为隐匿性感染......</p>
